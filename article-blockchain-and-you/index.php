                <div class="row pt-40">
					<div class="col-md-12">
						<p class="text-white">If you’ve ever sat down with a parent or grandparent and tried to explain blockchain, you know how difficult a concept it can be to lay out in simple terms. But one of the reasons it may be hard for you to explain is that you don’t really understand how it might affect the average person’s life.</p>
						<p class="text-white">Blockchain isn’t a currency or a language or a code, it’s an entire technology. As its name implies it’s a set of “blocks” or transactions that are linked together and shared among the users. The reason this is important is that it is incorruptible, and that lends security. It’s basically a digital ledger of transactions that can record not only financial transactions but almost anything else that is considered valuable. The usefulness of this is that it is at the same time secure (due to being incorruptible) and independent (since it doesn’t require an external authority to validate the authenticity and integrity of the data).</p>
						<p class="text-white pt-2"><strong>So how does it work?</strong></p>
						<p class="text-white">The blockchain has five components that allow it to work the way it does:</p>
						<p class="text-white"><strong>1. The Hash</strong> - A hash is a cryptographic function that changes any data that you give it into a
string of numbers and letters. It’s like a code for that particular data. And any time you enter
that data, the hash will give the same result. But it’s complex enough that you’ll never get
that same string from different data. So, it’s like a guaranteed code for that data. Importantly,
you can’t reverse a hash, so the original data that created it remains secure. And even more
importantly, each block of functions on the blockchain contains the hash of the previous
block as well. That way, if the data is moved or changed, you’ll know it, because it will no
longer have the right mark of the block next to it.
</p>
						<p class="text-white"><strong>2. The Ledger</strong> - The ledger created by blockchain uses the hash function just mentioned. This
incorruptible nature is what makes it immutable, or unchangeable. You can’t change any
block in the chain without changing the whole chain. Therefore, the whole chain works like
non-counterfeitable ledger of the transactions in it: like a tamper-proof lock. If any of the
items in the chain are changed, the ones around it will be wrong, because they won’t link to
the proximal block.

							</p>
						<p class="text-white"><strong>3. The Peer-to-Peer Network</strong> - One of the hallmarks of currencies and value transactions is
that they tend to rely on a standard. This gives the currency value and reliability. What the
blockchain does is obviate the need for this outside standard. It doesn’t need an internal or
external trust authority. The reason for this is that the data is distributed among all the users,
creating multiple copies of any transaction, all of which are protected by the cryptographic
hashes and the immutable ledger. Any time anything in the chain changes, copies of this are
sent to the entire network. So, in essence, the data isn’t stored in any one place, but in all of
them at once. It’s like quantum physics for transactions! And the benefit is that once a block
of transactions is validated, it becomes part of the chain and every user’s local information is
updated. Even if an inside or outside attacker modifies the local chain, the network won’t
accept any block from the altered blockchain, as it knows it’s corrupt.
</p>
						<p class="text-white"><strong>4. The Consensus Validation Protocol</strong> - When new blocks are added to a chain, all the users
have to approve or validate the new block or transaction for it to be accepted. The most
common way the users reach a consensus about the correctness of a new block by is by so called “Proof of Work” or “Proof of Stake”. They check that the new block meets the
standard of their proof method, including validation of all the transactions inside the block.
If it’s valid, they consider it part of the Blockchain and keep adding new blocks. If users
have different chains that show as valid, they will select the longest chain as the main
Blockchain and discard the shorter one.
</p>
						<p class="text-white"><strong>5. Block Validation/Mining</strong> - This aspect of the Blockchain is considered optional, but is an
important feature of many Blockchain uses, including Bitcoin and some other similar
currencies. This validation specifically describes meeting the “Proof of Work” standard for
validating transactions. This method involves the user creating a block with restrictions on
its Hash code. Since the Hash code is unpredictable, it is necessary to test any possible
combination before meeting the requirements. These restrictions define how difficult the
network is.
</p>
						<p class="text-white pt-3">How can this be used?</p>
						<p class="text-white"><u>Currency:</u> What the blockchain does is give us the ability to track and verify data. This is great
because we can therefore use a blockchain to track the transfer of something that has real world value, like digital currency, since we have a way to determine which item (e.g. “coin”) is being transferred, and also to and from whom. And it gives us a verifiable trail that can tell us whether the sender has the right to send it since we can also verify if they were the last person to have received it and therefore are actually in possession of it. It’s like a digital certificate of ownership. And because the record exists and can’t be expunged or changed, we know that the transaction has been made, preventing anyone from transacting with the same item more than once. That’s the equivalent of the real-world currency standard that an object like gold provides. There’s an actual verifiable “thing” that the value is attached to. It’s just a digital thing in this case. And it’s a thing that can’t be counterfeited, because of the unique hashes attached. This helps ensure and preserve value. Once this has all been done, it makes the entire system viable for sending payments and exchanging value. And that is how currencies can be based on it. Not only does blockchain let you exchange money faster, more efficiently and more securely, but many banks are already incorporating it into their transactions.
</p>
						<p class="text-white">Devices and Appliances: The so-called Internet of Things includes everything from cars to buildings to devices in the home. There will come a time when all the items in our homes will have embedded software and connectivity. But with this technology comes the problem that hackers can gain access to your devices via a central location that handles their communication. But Blockchain could potentially address these security concerns, since it takes the data off the centralized location and encodes it to protect its contents. This will get more and more important as our devices get smarter and smarter.</p>
						<p class="text-white"><u>Cybersecurity:</u> As we become more and more dependent on the digital world, our data becomes more and more critical. Because Blockchain’s encryption is so advanced, and its information so hard to falsify, it is very resistant to unauthorized changes and hacks. </p>
						<p class="text-white">Centralized servers can be very susceptible to data loss, corruption, human error and hacking, but Blockchain can help re-secure them. Considering how many large companies and governments have been the victims of hacking, Blockchain will be a critical means of keeping important information secure, especially for cloud storage.</p>
						<p class="text-white">Healthcare: Currently, doctors need to secure referrals, prescriptions and other important private data. Keeping them in a central database would be risky, and inefficient. But Blockchain would allow medical information to be kept locally, while still making it easy to authenticate. Hospitals and other healthcare organizations could create a centralized and secure database, store medical records, and share them with only authorized parties. And prescriptions could be handled without all the current hassle.</p>
						<p class="text-white">Peer to Peer Rentals and Sales: Services like AirBnB and ridesharing apps take a cut of all
transactions to provide the platform and security. Blockchain can create decentralized peer-to-peer ride-sharing apps and can allow car owners to auto pay for things like parking, tolls and fuel without relying on a middleman.
</p>
						<p class="text-white">Non-Profit Efficiency: As it currently stands, a large part of charity donations and a huge proportion of the operating expenses of non-profits go somewhere aside from the mission of the organization itself. Blockchains can ensure that funds go where they are needed and not into peripheral needs.</p>
						<p class="text-white">They can be used to allow donors to see where their donations go through a secure and transparent ledger. The UN World Food Program is already using blockchain technology to allow refugees to purchase food without vouchers, cash or credit cards.</p>
						<p class="text-white">The bottom line is that Blockchain is opening new doors for the future, not only in finance, but in ways that affect every aspect of our lives. While people have a hard time explaining what it is, they won’t have a hard time seeing how it changes our future in very powerful ways.</p>
