						<p class="text-white">The COVID-19 pandemic is highlighting a truth we have known for some time: that important
scientific achievements are increasingly the result of teams building on the work of other teams
over many years. The traditional conception of heroic individual researchers is misleading to the public, and distorts scientific funding by discouraging the recognition of collaboration. Yet the Nobel Prizes continue to limit the number of individuals who can be recognized for a given
achievement to just three.1 It is time that high-profile scientific awards like the Nobel Prizes
catch up with reality.
</p>
						<p class="text-white">During the week of October 5th, the Nobel Prizes for 2020 will be announced. The Nobels for
science, which cover the fields of Physiology/Medicine, Chemistry, and Physics, have been
given out since 1901.* Over the nearly 120 years of bestowing prestige, a gold medal, and a
sum of money on their recipients, the way the Prizes are awarded has barely changed at all, even as the way the science is done has changed profoundly
</p>
						<p class="text-white">The Nobel Prizes in science are awarded most often to an individual, and at most two or three
individuals, who have made remarkable advances in their field. But while literature is, almost by
definition, a profoundly lonely undertaking, science is not. Science is a group endeavor—and has only become more so in the last few decades, as globalization and technology have allowed for increased cooperation across specialties, nationalities, and time.
</p>
						<p class="text-white">As science becomes increasingly complex, research has expanded into a growing number of
specialties and subspecialties. The questions being answered are more sophisticated and
complicated than they once were, requiring increased collaboration. For scientific articles
indexed in PubMed in the 41 years from 1975 through 2016, the typical number of authors
nearly tripled (from 1.9 to 5.67 per article). And in the ten years from 2005 to 2015, the number
of authors per article in the three most prominent medical journals increased by more than a
third, as did the number of articles featuring group authors.[3] Some medical papers have literally hundreds of co-authors.
</p>
						<p class="text-white">Massive-scale scientific collaboration may have had its kick-start in the 1960s with the advent of NASA’s Apollo Program, which employed more than 400,000 Americans. Later, in the 1990s,
the Human Genome Project saw twenty research Universities across six countries collaborate to
map the Human Genome. Today we are seeing the continued and vital importance of
collaborative science in the massive numbers of people involved in rapid vaccine development
and large international drug trials to fight the novel coronavirus.
</p>
						<p class="text-white">Yet the Nobels still can only be awarded to one, two, or three scientists. Why is this so critical to
address now? There are a couple of very important reasons.
</p>
						<p class="text-white">First, awarding the Prizes to individuals is out of step with the increased collaboration now
required of our world. There is increased collaboration not only between institutions and
individuals, but even between countries. These cooperative efforts toward breakthroughs in
understanding should be celebrated rather than obscured.
</p>
						<p class="text-white">The response to the COVID-19 pandemic is the perfect backdrop in which to glorify the work of
science <i>as a whole</i>. Earlier this year, despite mounting evidence that COVID-19 could be spread
by airborne transmission, the World Health Organization refused to acknowledge these findings
until 239 scientists from 32 countries around the world came together to publish an open letter. A lone scientist might have been marginalized as a quack; but those 239 scientists made it possible to save countless lives.
</p>
						<p class="text-white">Second, individual prizes mislead the public about the very nature of science, which in turn
affects public enthusiasm for funding basic research. This concept of singular scientific
achievement, perpetuated by major awards like the Nobel, Fields, and Turing Prizes, tends to
elevate the idea that only singular geniuses will save us. When we get too fascinated by the
concept that one brilliant person will change the world, we get misled about how good science is done. We encourage and fund individuals, instead of thinking about a process, a whole
collaborative system, that involves as much of the citizenry as possible. In other words, we are
incentivizing the wrong behavior.
</p>
						<p class="text-white">Further, the Prizes themselves actually provide an incentive not to cooperate. They offer a fixed
sum of money to the winners, regardless of their number. This means that in order to maximize
the benefits of winning, a scientist must collaborate as little as possible, despite the clear benefits of cooperation on the actual work. Instead, we must find a way to reward cooperation and avoid the oversimplified trope of the lone scientist, because current scientific advancement benefits much more from large groups working together than it does from smaller groups competing with each other. It is not enough to simply spread research funding among more researchers, we must encourage them specifically to work together. For massive scientific undertakings to be successful, a wide array of skills must be employed, including not only those of the researchers themselves, but also of skilled organizers, support staff, and interorganizational administrators.
</p>
						<p class="text-white">With a dwindling amount of government research funding available, it is natural to allocate
funding to the most lauded individuals first, and not to the entire system. But this tendency
ignores the importance of scientific collaboration in even the work of those high-profile
researchers, as funding is increasingly concentrated among fewer scientists. This in turn
decreases the diversity of ideas so desperately needed in responding to a global pandemic. It also slows progress.
</p>
						<p class="text-white">Good science isn’t sexy. Important, trustworthy scientific research — the kind that can change
the world and save lives — depends on a whole lot of people doing a whole lot of decidedly
unglamorous work over a long period of time. As research dollars have decreased (or simply
been funneled to a few people doing big, “Nobel-worthy” work), a lot of good and necessary
science simply doesn’t get done.
</p>
						<p class="text-white">Instead of waiting for superheroes, and for brilliant lightning-bolt types of breakthroughs, we
need to support concerted scientific efforts — and large, sophisticated research teams working
together — to solve the complex problems we face. A first step would be to recognize either
greater numbers of individual authors for a given Nobel Prize, or to allow the scientific Prizes to
honor organizations as well as individuals (as the Nobel Peace Prize already does). 

</p>
						<p class="text-white">Also, imperative will be abiding by Alfred Nobel’s own stated goal of honoring those who confer the "greatest benefit on mankind". All of them. To do that, we must recognize and encourage teams of scientists to work as one, rather than encouraging a lottery ticket type of mentality by creating diminishing returns for larger teams. By changing the narrow-minded and outdated nature of the world’s most prestigious scientific prizes, we could acknowledge the complexities of focused scientific research, as well as the amount of teamwork required to do it well.</p>
